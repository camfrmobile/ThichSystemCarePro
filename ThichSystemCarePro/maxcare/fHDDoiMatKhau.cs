		}

		private void FConfigInteract_Load(object sender, EventArgs e)
		{
			try
			{
				if (setting.GetValueInt("typeMatKhau") == 0)
				{
					rbMatKhauRandom.Checked = true;
				}
				else
				{
					rbMatKhauChiDinh.Checked = true;
				}
				txtMatKhau.Text = setting.GetValue("txtMatKhau");
				nudTimeOut.Value = setting.GetValueInt("nudTimeOut", 30);
				rbHuyNgauNhien_CheckedChanged(null, null);
				rbHuyTheoUid_CheckedChanged(null, null);
			}
			catch
			{
			}
		}

